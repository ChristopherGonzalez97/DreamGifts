    private void LlenarListaNuevoPack() {
        Object matriz[][]= new Object[articulosLista.size()][2];   
        for (int i = 0; i < articulosLista.size(); i++) {
            matriz[i][0]= articulosLista.get(i).getArticulo().getArtDescripcion();
            matriz[i][1]= articulosLista.get(i).getCantidad();
        }
        tblAddArticulos.setModel(new javax.swing.table.DefaultTableModel(matriz, new String [] {
                "Nombre", "Cantidad"})
           {
                boolean[] canEdit = new boolean [] {
                false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }        
           });
    }
    
  class RadioButtonRenderer implements TableCellRenderer {
   public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected,        boolean hasFocus, int row, int column) {
      if (value==null) return null;
         return (Component)value;
   }
}
class RadioButtonEditor extends DefaultCellEditor implements ItemListener {
   private JRadioButton button;
   public RadioButtonEditor(JCheckBox checkBox) {
      super(checkBox);
   }
   public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
      if (value==null) return null;
      button = (JRadioButton)value;
      button.addItemListener(this);
      return (Component)value;
   }
   public Object getCellEditorValue() {
      button.removeItemListener(this);
      return button;
   }
   public void itemStateChanged(ItemEvent e) {
      super.fireEditingStopped();
   }
}
     
