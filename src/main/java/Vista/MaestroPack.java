    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String nombre = lstArticulos.getSelectedValue();
        Articulo articuloMenos = new Articulo();
        for(Articulo art : articulos)
        {
            if(nombre.equalsIgnoreCase(art.getArtDescripcion()))
            {
                DetallePack dPack = new DetallePack();
                dPack.setArticulo(art);
                dPack.setCantidad(Integer.parseInt(txtCantidad.getText()));
                articulosLista.add(dPack);
                int precio = dPack.getArticulo().getArtPrecio()*Integer.parseInt(txtCantidad.getText());
                precio = Integer.parseInt(lblPrecioEstimado.getText())+precio;
                lblPrecioEstimado.setText(Integer.toString(precio));
               articuloMenos=art;
            }
        }
         articulos.remove(articuloMenos);
         LlenarListaNuevoPack();
         LlenarLista();
         txtCantidad.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

